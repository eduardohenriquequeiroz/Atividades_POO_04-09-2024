package poo;
public class Carro {

	String banco;
	String roda;
	String cor;
	String volante;
	String ligarFarol;
	String aceleração;
	String virarDireita;
	String virarEsquerda;

	public String getBanco() {
		return banco;
	}
	public void setBanco(String banco) {
		this.banco = banco;
	}
	public String getRoda() {
		return roda;
	}
	public void setRoda(String roda) {
		this.roda = roda;
	}
	public String getCor () {
		return cor;
	}
	public void setCor(String cor) {
		this.cor = cor;
	}
	public String getVolante() {
		return volante;
	}
	public void setVolante(String volante) {
		this.volante = volante;
	}
	public String getLigarFarol() {
		return ligarFarol;
	}
	public void setLigarFarol(String ligarFarol) {
		this.ligarFarol = ligarFarol;
	}
	public String getAceleração() {
		return aceleração;
	}
	public void setAceleração(String aceleração) {
		this.aceleração = aceleração;
	}
	public String getVirarDireita() {
		return virarDireita;
	}
	public void setVirarDireita(String virarDireita) {
		this.virarDireita = virarDireita;
	}
	public String getVirarEsquerda() {
		return virarEsquerda;
	}
	public void setVirarEsquerda(String virarEsquerda) {
		this.virarEsquerda = virarEsquerda;
	}
}
