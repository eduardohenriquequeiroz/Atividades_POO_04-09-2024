package poo;

public class Cliente {
	
	int id;
	String nome;
	int telefone;
	long cpf;
	int rg;
	String reclamar;
	String elogiar;
	String pagar;
	String pedir;
	
	public int getId() {
		return id;
	}
	public void setId (int id) {
		this.id = id ;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getTelefone() {
		return telefone;
	}
	public void setTelefone(int telefone) {
		this.telefone = telefone;
	}
	public long getCpf() {
		return cpf;
	}
	public void setCpf(long cpf) {
		this.cpf = cpf;
	}
	public int getRg() {
		return rg;
	}
	public void setRg(int rg) {
		this.rg = rg;
	}
	public String getReclamar() {
		return reclamar;
	}
	public void setReclamar(String reclamar) {
		this.reclamar = reclamar;
	}
	public String getElogiar() {
		return elogiar;
	}
	public void setElogiar(String elogiar) {
		this.elogiar = elogiar;
	}
	public String getPagar() {
		return pagar;
	}
	public void setPagar(String pagar) {
		this.pagar = pagar;
	}
	public String getPedir() {
		return pedir;
	}
	public void setPedir(String pedir) {
		this.pedir = pedir;
	}
}


