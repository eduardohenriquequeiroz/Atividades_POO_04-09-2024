package poo;

public class Animal {

	String especie;
	String fasesVida;
	String listra;
	String cor;
	String velocidade;
	String emitirSom;
	String voar;
	String comer;

	public String getEspecie() {
		return especie;
	}
	public void setEspecie(String especie) {
		this.especie = especie;
	}
	public String getFasesVida() {
		return fasesVida;
	}
	public void setFasesVida(String fasesVida) {
		this.fasesVida = fasesVida;
	}
	public String getListra() {
		return listra;
	}
	public void setListra(String listra) {
		this.listra = listra;
	}
	public String getCor() {
		return cor;
	}
	public void setCor(String cor) {
		this.cor = cor;
	}
	public String getVelocidade() {
		return velocidade;
	}
	public void setVelocidade(String velocidade) {
		this.velocidade = velocidade;
	}
	public String getEmitirSom() {
		return emitirSom;
	}
	public void setEmitirSom(String emitirSom) {
		this.emitirSom = emitirSom;
	}
	public String getVoar() {
		return voar;
	}
	public void setVoar(String voar) {
		this.voar = voar;
	}
	public String getComer() {
		return comer;
	}
	public void setComer(String comer) {
		this.comer = comer;
	}
}
